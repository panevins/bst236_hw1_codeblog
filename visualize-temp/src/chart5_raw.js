function interpolateColor(value, min, max) {
    const ratio = (value - min) / (max - min);
    const r = Math.floor(255 * ratio);
    const g = 0;
    const b = Math.floor(255 * (1 - ratio));
    return `rgb(${r},${g},${b}, 0.4)`;
}

